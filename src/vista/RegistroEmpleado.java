    private void lblEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEliminarMouseClicked

      
        int rolDelLogin = usu.getIdrol();
        String cedula = txt.getText().toString().trim();
        int cedulaBusqueda = Integer.parseInt(txt.getText());
        if (cedula == null) {
            return;
        } else if (cedula.equals("")) {
            return;
        }
        if (rolDelLogin==2&&r.buscarConRolesIguales(cedulaBusqueda)==2) {
         c.mensajeBusquedaNoEncontradoPorUsuarios();
        }else{
                 c.boton_eliminar(cedula);
        }
    }//GEN-LAST:event_lblEliminarMouseClicked

